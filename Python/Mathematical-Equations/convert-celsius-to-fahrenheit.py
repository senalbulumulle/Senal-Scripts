# convert-celsius-to-fahrenheit.py

# Description: This program allows the convertion from celsius to fahrenheit

# Make a variable called celsius that contains a number that is used 
# for convertion
celsius = 37.5

# Make a variable called fahrenheit to calculate the provided number in celsius
fahrenheit = (celsius * 1.8) + 32

# Display the output
print('%0.1f degree celsius is equal to %0.1f degree Fahrenheit' %(celsius,fahrenheit))